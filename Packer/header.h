PVOID ResolveAPI(const char* dllName, const char* apiName) {
	HMODULE hLib = LoadLibraryA(dllName);
	if (!hLib) {
		printf("Cannot load %s", dllName);
		exit(1);
	}

	PVOID apiAddress = GetProcAddress(hLib, apiName);
	if (!apiAddress) {
		printf("Cannot Resolve API %s", apiName);
		exit(1);
	}
	return apiAddress;
}


typedef struct _PE_HEADER {
	PIMAGE_DOS_HEADER dosHeader;
	PIMAGE_NT_HEADERS ntHeader;
	PIMAGE_SECTION_HEADER sectionHeader;

	_PE_HEADER(BYTE* base) {
		dosHeader = (PIMAGE_DOS_HEADER)base;
		ntHeader = (PIMAGE_NT_HEADERS)((BYTE*)dosHeader + dosHeader->e_lfanew);
		sectionHeader = (PIMAGE_SECTION_HEADER)((BYTE*)ntHeader + sizeof(IMAGE_NT_HEADERS));
	}
} PE_HEADER;


BOOL IsValidPE(BYTE* base) {
	IMAGE_DOS_HEADER dosHeader = *(PIMAGE_DOS_HEADER)base;
	IMAGE_NT_HEADERS ntHeader = *(PIMAGE_NT_HEADERS)(base + dosHeader.e_lfanew);
	BOOL isPE = dosHeader.e_magic == IMAGE_DOS_SIGNATURE;
	BOOL is32 = ntHeader.FileHeader.Machine == IMAGE_FILE_MACHINE_I386 &&
		ntHeader.OptionalHeader.Magic == IMAGE_NT_OPTIONAL_HDR32_MAGIC;
	BOOL isDLL = ntHeader.FileHeader.Characteristics & IMAGE_FILE_DLL;
	BOOL isNET = ntHeader.OptionalHeader.
		DataDirectory[IMAGE_DIRECTORY_ENTRY_COM_DESCRIPTOR].Size != 0;

	if (!isPE || !is32 || isDLL || isNET) {
		printf("Not valid PE file!");
		return FALSE;
	}
	return TRUE;
}


DWORD CalculateAlignAddress(DWORD sectionAddress, DWORD sectionSize, DWORD alignValue) {
	DWORD virtualAddr = sectionAddress + alignValue;
	while (virtualAddr < (sectionAddress + sectionSize)) {
		virtualAddr += alignValue;
	}
	return virtualAddr;
}

